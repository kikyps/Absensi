package com.kp.absensi.user.ui.absen;

public class absensaya {
    String saya, kamu;

    public absensaya(String saya, String kamu) {
        this.saya = saya;
        this.kamu = kamu;
    }

    public String getSaya() {
        return saya;
    }

    public void setSaya(String saya) {
        this.saya = saya;
    }

    public String getKamu() {
        return kamu;
    }

    public void setKamu(String kamu) {
        this.kamu = kamu;
    }
}
